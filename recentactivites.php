            <div class="col-xs-7 faculty" style="margin-top: 0px">On 23rd May, 2020 at the
                Peak of COVID-19 Pandemic, the Team of Meatcryst Club under the guidance of
                our Head Mr. Saksham Popli came up with an event of Logo Designing, exclusive 
                for IITI students to rejuvenate their spirits during those dull times. 
                The Logo Designing Competition was named as MOTIF’20. The participants were 
                given a time frame of 1 week to submit their logos and the Prize was that the 
                winning logo will be the Official Logo of MetaCryst Club, IIT Indore along 
                with an Official Appreciation certificate from Metallurgy Engineering and 
                Materials Science Department of IIT Indore. The main theme of the competition was, how creative you can go that is equally related to the ideologies and work of MetaCryst Club. 
After one week, by 30th May, we had already received an overwhelming amount of logos 
(more than 30), with participation coming from Btech, Mtech and Phd level throughout all the departments. The Jury for the competition included the Faculty Coordinators of MetaCryst Club, Dr. Eswar Prasad Korimilli and Dr. Hemant Borkar. As stated by Jury members it was very hard for them to finalize the winner as all the logos were equally attractive, but going with the theme of the Competition the Jury finalized Mr. Archit Ojha, a Btech 2nd year undergraduate (at the time of competition) to be the final winner on 30th May, 2020. His created logo is still the official logo of MetaCryst Club.
We want to thank our Faculty coordinators Dr. Eswar Prasad Korimilli and Dr. Hemant Borkar 
for their support as the Jury and our Club Head Mr. Saksham Popli for his idea of organizing 
the competition at such an amazing time, when it was needed the most. The competition met with 
rave reviews and critical praise within the institution for its healthy organization without 
baffling up the results.

